# How Join Tuple Works:

# Using (+) Operator,we can join two or more tuples:
x = ('a','b','c')
y = (1,2,3)
z = x + y
print(z)
# Output: ('a', 'b', 'c', 1, 2, 3)

# Using (*) Operator, we can multiply the content in the tuple:
x = ('a','b','c')
y = x * 2
print(y)
# Output: ('a', 'b', 'c', 'a', 'b', 'c')
