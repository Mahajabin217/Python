# How Conditions & if else statements Works:

a = 30
b = 50

if(a == b):
    print('equal')
else:
    print('not equal')

if(a < b):
    print('a is less than b')
else:
    print('b is greater than a')

if(a > b):
    print('a is greater than b')
else:
    print('b is less than a')


""" Output: not equal
            a is less than b
            b is less than a
"""