# How Conditions & If Statements Works:

a = 30
b = 50

if(a == b):
    print('equal')

if(a != b):
    print('not equal')

if(a < b):
    print('a is less than b')

if(a <= b):
    print('a is less than or equal to b')

if(a > b):
    print('a is greater than b')

if(a >= b):
    print('a is greater than or equal to b')


""" Output: not equal
            a is less than b
            a is less than or equal to b
"""

