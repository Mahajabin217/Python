# How to Access Set Items Works:

# Loop through we can print the Set:
s = {'a','b','c'}
for x in s:
    print(x)
""" Output: a
            b
            c
"""
# Checking if a item is prsent in a Set:
s = {'a','b','c'}
print('b' in s)
# Output: True

# Checking if a item is Not present in a Set:
s = {'a','b','c'}
print('b' not in s)
# Output: False