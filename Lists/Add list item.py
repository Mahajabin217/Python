# How To Add Items in a List:

# The insert() method inserts an item at the specified index:
List = ['a','b','c','d','e']
List.insert(2, 'k')
print(List)

# Output: ['a', 'b', 'k', 'c', 'd', 'e'] (k is inserted in index 2)

# The append() method to add an item at the end of the list:
List = ['a','b','c','d','e']
List.append('k')
print(List)

# Output: ['a', 'b', 'c', 'd', 'e', 'k'] (k is added at the end of the list)