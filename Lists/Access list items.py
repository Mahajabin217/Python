# How List items can be accessed in different ways:

# How to access list items by Index number:
List = ["a", "b", "c"]
print(List[1])

# Output: b

# How to access last list items by Negative Index number:
print(List[-1])

# Output: c (last one)

# How to access list items by Range of Indexes:
L = ['d', 'e', 'f', 'g', 'h', 'i', 'j', 'k']
print(L[2:5])

# Output: ['f', 'g', 'h'] (Start at index 2 & end at index 5)

# How to access list items by Range of Negative Indexes:
L = ['d', 'e', 'f', 'g', 'h', 'i', 'j', 'k']
print(L[-4:-1])

# Output: ['h', 'i', 'j'] (Returns the index -4 to index -1)
