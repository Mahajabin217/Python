# How List operations can be performed:

# Creating a List: 
List = ["a", "b", "c"]
print(List)

# Output:  ['a', 'b', 'c']

# How to print the length of a list:
print(len(List))

# Output: 3

# List can contain different datatypes:
List_1 = ["abc", 30, True, "gf"]
print(List_1)

# Output:  ['abc', 30, True, 'gf']

