# How Sequence Datatypes(list,tuple,range) work:

# list:
a = ['ritu','riya','rima']
a[1] = 'rina' # list data can be changed
print(a) 
print(a[1])

"""Output:['ritu', 'rina', 'rima']
          rina
"""

# tuple:
b = ('raj','raja','riyad')
print(b)

#Output: ('raj', 'raja', 'riyad')

# range:
c = range(5)
print(c)

#Output: range(0, 5)