# How To Create An Array:

import array

# Creating an Array of intergers numbers:
int_ar = array.array('i',[1,2,3])
print(int_ar)

# Output: array('i', [1, 2, 3])

# Creating an Array of floating numbers:
float_ar = array.array('f',[1.1,1.2,1.3])
print(float_ar)

# Output: array('f', [1.100000023841858, 1.2000000476837158, 1.2999999523162842])
