# How Comparison Operators(==, !=, >, <, >=, <=) Works:

x = 5
y = 3

# Equal(==):
print(x == y)
# Output: False

#Not Equal(!=):
print(x != y)
# Output: True

# Greater than(>):
print(x > y)
# Output: True

# Less than(<):
print(x < y)
# Output: False

# Greater Than or Equal To(>=):
print(x >= y)
# Output: True

# Less Than or Equal To(<=):
print(x <= y)
# Output: False

