# How Comparison Operators(==, !=, >, <, >=, <=) Works:

x = 5
y = 3

# Equal(==):
print(x == y)

#Not Equal(!=):
print(x != y)

# Greater than(>):
print(x > y)

# Less than(<):
print(x < y)

# Greater Than or Equal To(>=):
print(x >= y)

# Less Than or Equal To(<=):
print(x <= y)

