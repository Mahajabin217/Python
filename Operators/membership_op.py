# How Membership Operators(in, not in) Works:

# in operator:

x = ["apple", "banana"]

print("banana" in x)
print("dragon" in x)

"""Output: True
           False
"""

# not in operator:

x = ["apple", "banana"]

print("pineapple" not in x)
print("apple" not in x)

"""Output: True
           False
"""