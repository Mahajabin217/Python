#How Arithmetic Operators(+,-,*,/,%,**,//) Work:

a = 15
b = 4

#Addition(+):
print(a+b)

#Subtraction(-):
print(a-b)

#Multiplication(*):
print(a*b)

#Division(/):
print(a/b)

#Modulus(%):
print(a%b)

#Exponentiation(**):
print(a**b)        

#Floor Division(//):
print(a//b)

