#How Arithmetic Operators(+,-,*,/,%,**,//) Work:

a = 15
b = 4

#Addition(+):
print(a+b)
# Output: 19

#Subtraction(-):
print(a-b)
# Output: 11

#Multiplication(*):
print(a*b)
# Output: 60

#Division(/):
print(a/b)
# Output: 3.75

#Modulus(%):
print(a%b)
# Output: 3

#Exponentiation(**):
print(a**b)        
# Output: 50625  (15*15*15*15=50625)

#Floor Division(//):
print(a//b)
# Output:  3      (It's value is 3.75,but it'll give us floor value 3)

